const scrollToBottom = (force = false) => {
  nextTick(() => {
    if (messagesRef.value) {
      const container = messagesRef.value

      // Ê£ÄÊü•ÊòØÂê¶ÈúÄË¶ÅÊªöÂä®Âà∞Â∫ïÈÉ®
      const isNearBottom =
        force || container.scrollHeight - container.scrollTop - container.clientHeight < 150

      if (isNearBottom) {
        // ‰ΩøÁî® smooth ÊªöÂä®ÔºåÈô§ÈùûÂº∫Âà∂ÊªöÂä®
        container.scrollTo({
          top: container.scrollHeight,
          behavior: force ? 'auto' : 'smooth',
        })
      }
    }
  })
}

// Âº∫Âà∂ÊªöÂä®Âà∞Â∫ïÈÉ®ÁöÑËæÖÂä©ÂáΩÊï∞
const forceScrollToBottom = () => {
  scrollToBottom(true)
  // ÊªöÂä®ÂêéÈöêËóèÊªöÂä®ÊåâÈíÆ
  showScrollToBottom.value = false
}

// Ê£ÄÊü•ÊòØÂê¶ÈúÄË¶ÅÊòæÁ§∫ÊªöÂä®Âà∞Â∫ïÈÉ®ÊåâÈíÆ
const checkScrollPosition = () => {
  if (messagesRef.value) {
    const container = messagesRef.value
    const isNearBottom = container.scrollHeight - container.scrollTop - container.clientHeight < 150
    showScrollToBottom.value = !isNearBottom && messages.value.length > 0
  }
}

// Ê∑ªÂä†ÊªöÂä®ÁõëÂê¨Âô®
const addScrollListener = () => {
  if (messagesRef.value) {
    messagesRef.value.addEventListener('scroll', checkScrollPosition)
  }
}

// ÁßªÈô§ÊªöÂä®ÁõëÂê¨Âô®
const removeScrollListener = () => {
  if (messagesRef.value) {
    messagesRef.value.removeEventListener('scroll', checkScrollPosition)
  }
}

